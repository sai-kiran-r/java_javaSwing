    private void btnModelyearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModelyearActionPerformed
        // TODO add your handling code here:
        String yearTxt = txtModelyear.getText();
        if(yearTxt.isEmpty()){
            JOptionPane.showMessageDialog(this, "Please enter Manufacturing Year");
            return;
        }
        DefaultTableModel model = (DefaultTableModel) tblModelyear.getModel();
        model.setRowCount(0);
        
        for(Product vs : history.getHistory())
        {
            if(vs.getCarYear() == Integer.parseInt(yearTxt.trim())){
                Object[] row = new Object[8];
            row[0] = vs;
            row[1] = vs.getCarYear();
            row[2] = vs.getCarSeats();
            row[3] = vs.getCarSerialnum();
            row[4] = vs.getCarModelnum();
            row[5] = vs.getCarAvaliablity();
            row[6] = vs.getCarCity();
            row[7] = vs.getCarExpired();
            
            model.addRow(row);
            }
        }
        
        
    }//GEN-LAST:event_btnModelyearActionPerformed


