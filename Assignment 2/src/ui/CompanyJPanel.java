    private void btnCsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCsearchActionPerformed
        // TODO add your handling code here:
        String companyTxt = txtCompany.getText();
        if(companyTxt.isEmpty()){
            JOptionPane.showMessageDialog(this, "Please enter Company Name");
            return;
        }
        DefaultTableModel model = (DefaultTableModel) tblCompany.getModel();
        model.setRowCount(0);
        
        for(Product vs : history.getHistory())
        {
            if(vs.getCarCompany().equals( companyTxt.trim())){
                Object[] row = new Object[8];
            row[0] = vs;
            row[1] = vs.getCarYear();
            row[2] = vs.getCarSeats();
            row[3] = vs.getCarSerialnum();
            row[4] = vs.getCarModelnum();
            row[5] = vs.getCarAvaliablity();
            row[6] = vs.getCarCity();
            row[7] = vs.getCarExpired();
            
            model.addRow(row);
            }
        }
        
        
    }//GEN-LAST:event_btnCsearchActionPerformed


