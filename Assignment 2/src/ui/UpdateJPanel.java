    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tblcars.getSelectedRow();
        if(selectedRowIndex<0){
            JOptionPane.showMessageDialog(this, "Select any row to update");
            return;
        }
        
        if( this.validateSerial()){
            JOptionPane.showMessageDialog(this, "Please enter valid serial");
            return;
        }
        
        if(txtCompany.getText().isEmpty() || txtYear.getText().isEmpty() ||
            txtSeats.getText().isEmpty() || txtSerial.getText().isEmpty() ||
            txtModel.getText().isEmpty() || txtAvail.getText().isEmpty() ||
            txtCity.getText().isEmpty() || txtExpiry.getText().isEmpty()
                ){
            JOptionPane.showMessageDialog(this, "Please enter all details");
            return;
        }
        
        
        
        DefaultTableModel model = (DefaultTableModel) tblcars.getModel();
        JTextField [] arrJTextFields = { txtCompany, txtYear, txtSeats, txtSerial,
                    txtModel, txtAvail, txtCity, txtExpiry};
        
        int cols = 0;
        for(JTextField eachField : arrJTextFields){
            model.setValueAt(eachField.getText(), selectedRowIndex, cols);
            cols++;
        }
        Product updateDetails = new Product(txtCompany.getText() , Integer.parseInt(txtYear.getText()), 
                                Integer.parseInt(txtSeats.getText()), Integer.parseInt(txtSerial.getText()),
                    txtModel.getText(), txtAvail.getText(), txtCity.getText() , txtExpiry.getText() );
        
        
        
        history.updateDetails(selectedRowIndex,updateDetails);
        LocalDateTime obj = LocalDateTime.now();
        catalogHistory.setLastUpdateTime(obj);
        
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tblcars.getSelectedRow();
        
        if(selectedRowIndex<0){
            JOptionPane.showMessageDialog(this, "Select any row to delete");
            return;
        }
        
        DefaultTableModel model = (DefaultTableModel) tblcars.getModel();
        Product selectProduct = (Product) model.getValueAt(selectedRowIndex, 0);
        
        history.deleteProducts(selectProduct);
        
        populateTable();
        
        
        
    }//GEN-LAST:event_btnDeleteActionPerformed

