    private boolean validateSerial(){
        int Serial = Integer.parseInt(txtSerial.getText());
        if(Product.unSerial.add(Serial)){
            return true;
        }
        return false;
    }
    
    
    
    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblcars.getModel();
        model.setRowCount(0);
        
        for(Product vs : history.getHistory()){
            
            Object[] row = new Object[8];
            row[0] = vs;
//            row[0] = vs.getCarCompany();
            row[1] = vs.getCarYear();
            row[2] = vs.getCarSeats();
            row[3] = vs.getCarSerialnum();
            row[4] = vs.getCarModelnum();
            row[5] = vs.getCarAvaliablity();
            row[6] = vs.getCarCity();
            row[7] = vs.getCarExpired();
            
            model.addRow(row);
            
            
        }
    
    }
